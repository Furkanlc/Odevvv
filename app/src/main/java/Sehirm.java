public class Sehirm {

    String sehirad;
    int plaka;

    public Sehirm(String sehirad, int plaka) {
        this.sehirad = sehirad;
        this.plaka = plaka;
    }

    public String getSehirad() {
        return sehirad;
    }

    public void setSehirad(String sehirad) {
        this.sehirad = sehirad;
    }

    public int getPlaka() {
        return plaka;
    }

    public void setPlaka(int plaka) {
        this.plaka = plaka;
    }
}
